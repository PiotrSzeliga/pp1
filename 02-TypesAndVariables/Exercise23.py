import math
a = int(input("Enter a: "))
b = int(input("Enter b: "))
c = int(input("Enter c: "))
circumference = a + b + c
half_circumference = circumference//2
area = math.sqrt(half_circumference(half_circumference-a)*(half_circumference-b)*(half_circumference-c))
print(f"Triangle area: {area}\n", "Triangle circumference: {}")

